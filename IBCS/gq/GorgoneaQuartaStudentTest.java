	public static void testGorgoneaQuarta01() {
		
		int[] decimals = new int[] {10, 2, -17, 42, 1024};
		String[] gqs = new String[] {"101", "1-", "-101", "1---0", "111-0-1"};
		
		for (int i = 0; i < 5; i++) {
			
			int decimal = toDecimal(gqs[i]);
			
			if (decimals[i] == decimal) {
				
				System.out.println("Succeeded toDecimal for value " + gqs[i]);
				
			} else {
				
				System.err.println("FAILED toGQ for value " + gqs[i] + "; expected "
					+ String.valueOf(decimals[i]) + " but got " + String.valueOf(decimal));
				
			}
			
		}
		
		for (int i = 0; i < 5; i++) {
			
			String gq = toGQ(decimals[i]);
			
			if (gqs[i].equals(gq)) {
				
				System.out.println("Succeeded toGQ for value " + String.valueOf(decimals[i]));
				
			} else {
				
				System.err.println("FAILED toGQ for value " + String.valueOf(decimals[i]) + "; expected "
					+ gqs[i] + " but got " + gq);
				
			}
			
		}
		
	}
	
